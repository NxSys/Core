/**
 * SplFileObject wrapper
 *
 * This class is technically a wrapper (or facade) for the internal PHP class.
 * It also serves as a concretion of the respective interface so that its type
 * may be used without having to create a reference class yourself.
 *
 * This class will implement a trait that allows direct proxying of calls to an
 * underlying target object. Regardless of that magic, methods are "implemented"
 * (but stubbed) so as to formally fulfill the contract required by the interface.
 *
 * Occasionally if an internal method returns one of these wrapped objects it
 * will be *rewrapped* before being returned.
 *
 * @see \SplFileObject
 * @link http://php.net/manual/en/class.splfileobject.php
 * @author Chris R. Feamster <cfeamster@f2developments.com>
 */
class SplFileObject extends StdLib\SplFileInfo implements \RecursiveIterator, \Traversable, \Iterator, \SeekableIterator, StdLib\ISplFileObject, StdLib\IhasTraitDecorating
